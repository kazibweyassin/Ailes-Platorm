// Enhanced duplicate checking - checks name, provider, and website
async function checkDuplicate(name: string, provider: string, website?: string): Promise<boolean> {
  const whereConditions: any[] = [
    { name: { equals: name, mode: 'insensitive' as const } },
    { provider: { equals: provider, mode: 'insensitive' as const } }
  ]
  
  if (website) {
    whereConditions.push({ website: { equals: website, mode: 'insensitive' as const } })
  }
  
  const existing = await prisma.scholarship.findFirst({
    where: {
      OR: whereConditions
    }
  })
  return !!existing
}

